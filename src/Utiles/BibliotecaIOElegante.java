package Utiles;

public class BibliotecaIOElegante {
   
    public static void recuadroGuiones(String texto){
        System.out.println("-----------");
        System.out.println(texto);
        System.out.println("-----------");
    }
    
    public static void recuadroPuntos(String texto){
        System.out.println("............");
        System.out.println(texto);
        System.out.println("............");
    }
    
    public static void recuadroCombinado(String texto){
        System.out.println(">-<->-<");
        System.out.println(texto);
        System.out.println(">-<->-<");
    }
  
}
